int	ft_atoi(char *str)
{
	int	nb;
	int	i;
	int	sg;

	i = 0 ;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n' || \
			str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	sg = 1;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sg = sg * -1;
		i++;
	}
	nb = 0;
	while (str[i] <= '9' && str[i] >= '0')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * sg);
}
